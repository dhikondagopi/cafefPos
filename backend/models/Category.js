const mongoose = require('mongoose');

const CategorySchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  description: {
    type: String,
    trim: true
  },
  color: {
    type: String,
    default: '#FF5722'
  }
}, {
  timestamps: true
});

module.exports = mongoose.model('Category', CategorySchema);
